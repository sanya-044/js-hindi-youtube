//pre increment (change, then use)
let age=10;
let newAge=++age; // first age val will be updated to 11 and then its assigned to newAge
console.log(newAge)
console.log(age)


//post increment (use,then change)
let agee=10
let newAgee=agee++  //first agee's old val will be stored to newAgee thats 10 and then agee val will be updated to 11 
console.log(agee)
console.log(newAgee)